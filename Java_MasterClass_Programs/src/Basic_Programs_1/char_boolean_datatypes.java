package Basic_Programs_1;

public class char_boolean_datatypes {
	public static void main(String[] args) {
		char myChar = 'D';
		char myUnicodeChar = '\u0044'; //unicode is an international encoding standarad for each letter,digit or symbol is assigned a numeric value
		System.out.println(myChar);
		System.out.println(myUnicodeChar);
	
		char myUnicodeChar1 = '\u0049';
		
		System.out.println(myUnicodeChar1);
		
		//-------------boolean datatype
		
		boolean myTrueBooleanValue = true;
		boolean myFalseBooleanValue = false;
		
		System.out.println(myTrueBooleanValue);
		System.out.println(myFalseBooleanValue);
		
		
	}
}
